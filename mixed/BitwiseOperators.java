public class BitwiseOperators{
   public static void main(String[] args) {
       int a=6;
       int b=7;
       System.out.println(a|b);
       System.out.println(a&b);
       System.out.println(a^b);
       a=12;
       int shift=a << 2;
       System.out.println(shift);
       a=20;
       shift=a<<2;
       System.out.println(shift);
   }

}
